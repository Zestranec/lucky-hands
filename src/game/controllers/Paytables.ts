import type { VolatilityMode } from '../models/Types';

// ─── RTP Verification ────────────────────────────────────────────────────────
// P(win per tile) = 1/3.  w = number of wins in 5 tiles.
// P(w wins) = C(5,w) * (1/3)^w * (2/3)^(5-w)
//
//   P(0) = 32/243 ≈ 0.13169
//   P(1) = 80/243 ≈ 0.32922
//   P(2) = 80/243 ≈ 0.32922
//   P(3) = 40/243 ≈ 0.16461
//   P(4) = 10/243 ≈ 0.04115
//   P(5) =  1/243 ≈ 0.00412
//
// LOW:  E[mult] = 0.57 * E[w] = 0.57 * (5 * 1/3) = 0.57 * 1.6667 ≈ 0.9500  ✓
//
// MED:  E[mult] = 0*P(0..1) + 1.10*P(2) + 2.10*P(3) + 3.80*P(4) + 20.80*P(5)
//             = (1.10*80 + 2.10*40 + 3.80*10 + 20.80*1) / 243
//             = (88 + 84 + 38 + 20.80) / 243 = 230.80/243 ≈ 0.9501  ✓
//
// HIGH: E[mult] = 0*P(0..2) + 2.60*P(3) + 11.20*P(4) + 14.90*P(5)
//             = (2.60*40 + 11.20*10 + 14.90*1) / 243
//             = (104 + 112 + 14.90) / 243 = 230.90/243 ≈ 0.9502  ✓

export const PAYTABLES: Record<VolatilityMode, (w: number) => number> = {
  LOW: (w) => 0.57 * w,

  MED: (w) => {
    if (w <= 1) return 0;
    if (w === 2) return 1.10;
    if (w === 3) return 2.10;
    if (w === 4) return 3.80;
    return 20.80; // w === 5
  },

  HIGH: (w) => {
    if (w <= 2) return 0;
    if (w === 3) return 2.60;
    if (w === 4) return 11.20;
    return 14.90; // w === 5
  },
};
